const int CASES = 3;

vector<long long> calc_divisor(long long n) {
    vector<long long> res;
    for (long long i = 1LL; i*i <= n; ++i) {
        if (n % i == 0) {
            res.push_back(i);
            long long j = n / i;
            if (j != i) res.push_back(j);
        }
    }
    sort(res.begin(), res.end());
    return res;
}

vector<pair<long long, long long> > prime_factorize(long long n) {
    vector<pair<long long, long long> > res;
    for (long long p = 2; p * p <= n; ++p) {
        if (n % p != 0) continue;
        int num = 0;
        while (n % p == 0) { ++num; n /= p; }
        res.push_back(make_pair(p, num));
    }
    if (n != 1) res.push_back(make_pair(n, 1));
    return res;
}

void solve() {
    ll N; cin >> N;
    if(N==2){
        cout << 1 << endl;
        return;
    }
    ll ans = 0;

    auto vec = calc_divisor(N);
    for(auto K : vec){
        if(K==1) continue;
        ll target = N;
        while(target >= K){
            if(target % K== 0) target /= K;
            else target %= K;
        }
        if(target==1){
            ++ans;
        }
    }

    auto vec2 = prime_factorize(N-1);
    ll tmp = 1;
    for(auto K : vec2){
        tmp *= (K.second+1);
    }
    ans += tmp-1;
    cout << ans << endl;
}

